
public class IncrementAndDecrementActivity
{

	public static void main(String[] args)
	{
		 int x = 2;
	     int number = x++;
	     System.out.println(number);
	     // displays 2

	     int x1 = 2;
	     System.out.println(x1++);
	     // displays 2

	    int x2 = 2;
	     System.out.println(--x2);
	     // displays 1

	     int x3 = 8;
	     int numberOne = x3--;
	     System.out.println(numberOne);
	     // displays 8
	     

	}

}
